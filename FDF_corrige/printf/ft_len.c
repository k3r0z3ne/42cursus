#include "ft_printf.h"

size_t	ft_len(long nb)
{
	size_t	count;

	if (nb < 0)
		nb *= -1;
	count = 1;
	while (nb / 10)
	{
		nb /= 10;
		count++;
	}
	return (count);
}
