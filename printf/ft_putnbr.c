#include "ft_printf.h"

int	ft_putnbr(int nb)
{
	int	signe;

	signe = 0;
	if (nb == -2147483648)
	{
		write(1, "-2147483648", 11);
		return (11);
	}
	if (nb < 0)
	{
		nb *= -1;
		ft_putchar('-');
		signe++;
	}
	if (nb > 9)
	{
		ft_putnbr(nb / 10);
		ft_putnbr(nb % 10);
	}
	else
		ft_putchar(nb + '0');
	if (signe == 1)
		return (ft_len(nb) + 1);
	return (ft_len(nb));
}
