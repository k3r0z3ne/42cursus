#include "libft.h"

char	*ft_strdup(char *src)
{
	int		srclen;
	char	*dup;
	int		i;

	srclen = ft_strlen(src);
	dup = ft_calloc((srclen + 1), sizeof(char));
	if (dup == NULL)
		return (NULL);
	i = 0;
	while (src[i] != '\0')
	{
		dup[i] = src[i];
		i++;
	}
	return (dup);
}
